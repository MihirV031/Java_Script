var total = 0
var items = 0
function get1()
{
    var x = document.getElementById('item').value
    var y = document.getElementById('item')
    var a = parseInt(x)
    if(y.checked == true)
    {
        total = total + a;
    }
    else
    {
        total = total - a;
    }
    console.log(total)

    console.log(x)
    document.getElementById('amount').value = total;
    document.getElementById('count').value = items;
}
